                    <h3 class="mt-3">Ekstrakurikuler</h3>

                    <div class="mt-25 article-body">
                        <p><img alt="" src="http://ditpsd.kemdikbud.go.id/upload/filemanager/2020/10/sda.jpg" style="float:left; height:819px; width:555px" /></p>

<p>Kegiatan ekstrakurikuler sebagai wadah pengembangan potensi peserta didik, dapat memberikan dampak positif dalam penguatan pendidikan karakter. Peserta didik diharapkan dapat mengembangkan karakter profil Pelajar Pancasila yaitu : (1) berkebinekaan global, (2) bergotong royong, (3) kreatif, (4) bernalar kritis, (5) mandiri, dan (6) beriman, bertakwa kepada Tuhan YME, dan berakhlak mulia.</p>

<p>Satuan pendidikan memiliki kewajiban untuk menyelenggarakan kegiatan ekstrakurikuler sebagai wahana memfasilitasi pengembangan bakat dan minat peserta didik. Oleh sebab itu, kegiatan ekstrakurikuler harus dikelola secara sistematis dan terpola agar bermuara pada pencapaian tujuan yang dimaksud. Agar dapat menyusun dan mengembangkan kegiatan ekstrakurikuler yang tersistem dan terpola sekolah perlu memahami cara dan tahapan diperlukan panduan yang dapat membimbingsatuan pendidikan dalam menyelenggarakannya.</p>

<p>Merujuk pada Peraturan Menteri Pendidikan dan Kebudayaan Republik Indonesia Nomor 62 tahun 2014 tentang Kegiatan Ekstrakurikuler Pada Pendidikan Dasar dan Pendidikan Menengah, ekstrakurikuler adalah kegiatan pengembangan karakter dalam rangka perluasan potensi, bakat, minat, kemampuan, kepribadian, kerja sama, dan kemandirian peserta didik secara optimal yang dilakukan di luar jam belajar kegiatan intrakurikuler dan kegiatan kokurikuler di bawah bimbingan dan pengawasan satuan pendidikan.</p>

<p>JENIS EKSTRAKURIKULER</p>

<p>Ekstrakurikuler wajib adalah kegiatan ekstrakurikuler yang wajib diselenggarakan oleh satuan pendidikan dan wajib diikuti oleh seluruh peserta didik. Kegiatan ekstrakurikuler Wajib yang dimaksud berbentuk pendidikan kepramukaan, yang diatur khusus dalam Peraturan&nbsp; Permendikbud RI Nomor 63 tahun 2014.</p>

<p>Ekstrakurikuler pilihan adalah kegiatan ekstrakurikuler yang dapat dikembangkan dan diselenggarakan oleh satuan pendidikan dan dapat diikuti oleh peserta didik sesuai bakat dan minatnya masing-masing. Pilihan bidang yang dikembangkan tiap sekolah akan berbeda-beda seperti eksktrakurikuler seni, olahragam sains, mapun keagamaan, dan lain-lain.</p>

<p>FUNGSI EKSTRAKURIKULER</p>

<p>Ekstrakurikuler di Sekolah Dasar memiliki fungsi:</p>

<ol>
	<li>Pengembangan, yaitu sebagai wahana pengembangan minat dan bakat peserta didik.</li>
	<li>Sosial, yaitu sebagai wahana untuk memperluas pengalaman bersosialisasi, praktik keterampilan berkomunikasi, dan internalisasi nilai-nilai karakter.</li>
	<li>Rekreatif,yaitu dilakukan dalam suasana gembira dan menyenangkan, sehingga suasana ini menunjang proses perkembangan potensi/kemampuan personal peserta didik.</li>
	<li>Persiapan Karir, yaitu sebagai wahana memfasilitasi persiapan peserta didik melalui pengembangan bakat dan minat dalam bidang ekstrakurikuler yang diminati.</li>
</ol>

<p>PRINSIP KEGIATAN EKSTRAKURIKULER</p>

<p>Ekstrakurikuler di sekolah dasar diselenggarakan dengan prinsip:</p>

<ol>
	<li>Partisipasi Aktif, yakni bahwa kegiatan ekstrakurikuler menuntut keikutsertaan peserta didik secara penuhsesuai dengan minat dan pilihan masing-masing.</li>
	<li>Menyenangkan,yakni bahwa kegiatan ekstrakurikuler dilaksanakan dalam suasana yang menggembirakan bagi peserta didik.</li>
</ol>

<p>SIFAT KEGIATAN EKSTRAKURIKULER</p>

<p>Kegiatan ekstrakurikuker di sekolah dasar diselenggarakan mengacu sifat-sifat berikut:</p>

<ol>
	<li>Individual, yakni dikembangkan sesuai dengan potensi/bakat peserta didik masing-masing.</li>
	<li>Pilihan, yakni dikembangkan sesuai dengan minat dan diikuti oleh peserta didik secara sukarela.</li>
	<li>Memotivasi, yakni membangun semangat peserta didik untuk mengembangkan potensi/bakat melalui kegiatan yang diminati.</li>
	<li>Kemanfaatan sosial, yakni dikembangkan dan dilaksanakan dengan tidak melupakan kepentingan masyarakat.</li>
</ol>

<p>BENTUK KEGIATAN EKSTRAKURIKULER DI SEKOLAH DASAR</p>

<p>Sebagaimana diatur dalam Permendikbud RI Nomor 62 tahun 2014 tentang Kegiatan Ekstrakurikuler Pada Pendidikan Dasar dan Pendidikan Menengah, bentuk kegiatan ekstrakurikuler dapat berupa:</p>

<ol>
	<li>Krida, misalnya: Kepramukaan, Latihan Kepemimpinan Siswa (LKS), Palang Merah Remaja (PMR), Usaha Kesehatan Sekolah (UKS), Pasukan Pengibar Bendera (Paskibra), dan lainnya;</li>
	<li>Karya ilmiah, misalnya: Kegiatan Ilmiah Remaja (KIR), kegiatan penguasaan keilmuan dan kemampuan akademik, penelitian, dan lainnya;</li>
	<li>Latihan olah-bakat dan olah-minat, misalnya: pengembangan bakat olahraga, seni dan budaya, pecinta alam, jurnalistik, teater, teknologi informasi dan komunikasi, rekayasa, dan lainnya;</li>
	<li>Keagamaan, misalnya: Tahfiz QUR&rsquo;AN, baca tulis ALQUR&rsquo;AN, marawis, retreat; atau</li>
	<li>Bidang pengembangan lainnya, yang disesuaikan dengan prioritas dan analisis potensi dan minat peserta didik di sekolah.</li>
</ol>

<p>Sekolah perlu menentukan pilihan prioritas kegiatan ekstrakurikuler yang akan diselenggarakan berdasarkan analisis potensi dan minat peserta didik,serta kemampuan sekolah dalam memenuhi sumberdaya yang dibutuhkan dalam penyelenggaraan kegiatan ekstrakurikuler. Sekolah dapat mengembangkan bentuk kegiatan selain daripada yang tersebut di atas berdasarkan kearifan lokal dan kondisi sosial masyarakat di lingkungan sekolah dengan tetap memerhatikan tujuan ekstrakurikuler di sekolah dasar.</p>

<p>Satuan pendidikan juga perlu memikirkan daya dukung lain untuk kesinambungan pembinaan kegiatan ekstrakurikuler yang diprogramkan. Daya dukung lain misalnya menyediakan kegiatan yang bersifatkompetitif-prestatif bagi peserta didik yang mengikuti kegiatan ekstrakurikuler.Kegiatan kompetitif-prestatif yang dapat dilakukan misalnya menyelenggaraan perlombaan/kompetisi keterampilan ekstrakurikuler di tingkat satuan pendidikan, mengikutsertakan peserta didik yang dibina melalui ekstrakurikuler dalam kegiatan festival, lomba, olimpiade, atau kegiatan kompetitif-prestatif lainnya.</p>

<p>Kegiatan kompetitif-prestatif dapat menjadi salah satu bentuk evaluasi pelaksanaan ekstrakurikuler di satuan pendidikan. Dengan melihat prestasi peserta didik dalam sebuah kompetisi, tim pembina dapat melakukan evaluasi terhadap program ekstrakurikuler serta mengembangkannya menjadi lebih baik pada masa berikutnya. Di sisi lain, melalui kegiatan kompetitif dapat meningkatkan rasa percaya diri anak terhadap hasil usaha latihannya dalam mengikuti kegiatan ekstrakurikuler.</p>
